void hdcd_decode::process_sample_lsb( int sample )
{
	unsigned bit = sample & 1;
	bits_a = ( bits_a * 2 ) | bit;
	bit = ( ( bits_a >> 5 ) ^ bits_a ^ ( bits_a >> 23 ) ) & 1;
	bits_b = ( bits_b * 2 ) | bit;
	if ( step_counter == 0 )
	{
		if ( ( bits_b & 0xFFFFFF ) == 0x7E0FA0 )
		{
			step_counter = 1;
		}
	}
	else if ( step_counter < 16 )
	{
		step_counter++;
	}
	else if ( step_counter == 16 )
	{
		bit = bits_b & 0xFF00;
		if ( bit == 0x500 )
		{
			if ( ! ( bits_b & 0xC8 ) )
			{
				status = ( bits_b & 0x30 ) | ( ( bits_b & 0x07 ) * 2 );
				sample_counter = sample_rate + 1;
			}
			step_counter = 0;
		}
		else if ( bit == 0x600 )
		{
			step_counter++;
		}
		else
		{
			step_counter = 0;
		}
	}
	else if ( step_counter > 16 )
	{
		if ( step_counter != 24 )
		{
			step_counter++;
		}
		else
		{
			bit = ( bits_b >> 8 ) ^ bits_b;
			if ( ! ( unsigned char ) ( bit + 1 ) )
			{
				status = bits_b >> 8;
				sample_counter = sample_rate + 1;
			}
			step_counter = 0;
		}
	}
	if ( sample_counter )
	{
		sample_counter--;
	}
	else
	{
		status = 0;
	}
}

hdcd_decode::hdcd_decode()
{
	reset();
}

void hdcd_decode::reset()
{
	bits_a = 0;
	bits_b = 0;
	step_counter = 0;
	status = 0;
	sample_counter = 0;
	gain_current = 1;
	gain_running = 1;
	gain_fixed = 4096;
}

int hdcd_decode::decode_sample( int sample )
{
	process_sample_lsb( sample );

	if ( sample != (short)sample ) sample = 0x7FFF ^ ( sample >> 31 );

	if ( sample_counter )
	{
		gain_current = gain_table[ status & 15 ];

		if ( gain_running > gain_current )
		{
			gain_running *= 0.99955034255981445;
			if ( gain_running < gain_current ) gain_running = gain_current;
			gain_fixed = (int)( ( gain_running * 4096 ) + .5 );
		}
		else if ( gain_running < gain_current )
		{
			gain_running *= 1.00360429286956809;
			if ( gain_running > gain_current ) gain_running = gain_current;
			gain_fixed = (int)( ( gain_running * 4096 ) + .5 );
		}
	}

	if ( abs( sample ) > 0x5980 )
	{
		if ( status & 16 )
		{
			if ( sample >= 0 )
			{
				unsigned index = sample - 0x5981;
				sample = peak_extend_table [index];
				sample = sample >> 4;
			}
			else
			{
				unsigned index = -sample - 0x5981;
				sample = peak_extend_table [index];
				sample = -sample >> 4;
			}
		}
		else
		{
			sample <<= 3;
		}
	}
	else
	{
		sample <<= 3;
	}

	sample = ( sample * gain_fixed ) >> 12;

	return sample;
}

